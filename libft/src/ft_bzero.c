#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*p;
	size_t	i;

	i = 0;
	if (!s)
		return ;
	p = (char *)s;
	while (i < n)
	{
		p[i] = '\0';
		i++;
	}
}
